-- =====================================================
-- wecreate 数据库建库建表脚本
-- 创建数据库：wecreate
-- 包含表：WECREATE_CLI_LOG, WECREATE_CLI_PROMPT, WECREATE_PROJECT
-- =====================================================

-- 创建数据库
CREATE DATABASE IF NOT EXISTS `wecreate` DEFAULT CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;

USE `wecreate`;

-- =====================================================
-- 表：WECREATE_CLI_LOG
-- 描述：自定义日志追踪表
-- =====================================================
DROP TABLE IF EXISTS `WECREATE_CLI_LOG`;

CREATE TABLE `WECREATE_CLI_LOG` (
  `ID` BIGINT NOT NULL AUTO_INCREMENT,
  `CREATE_USER` VARCHAR(50) NOT NULL,
  `CREATE_TIME` TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
  `TRACE_ID` VARCHAR(255) NOT NULL,
  `TYPE` VARCHAR(255) NOT NULL,
  `CONTENT` LONGTEXT,
  `LM_USER` VARCHAR(50),
  `LM_TIME` TIMESTAMP NULL,
  PRIMARY KEY (`ID`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='自定义日志追踪表';

-- 添加列注释
ALTER TABLE `WECREATE_CLI_LOG` MODIFY COLUMN `ID` BIGINT NOT NULL AUTO_INCREMENT COMMENT '主键';
ALTER TABLE `WECREATE_CLI_LOG` MODIFY COLUMN `CREATE_USER` VARCHAR(50) NOT NULL COMMENT '创建人';
ALTER TABLE `WECREATE_CLI_LOG` MODIFY COLUMN `CREATE_TIME` TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL COMMENT '创建时间';
ALTER TABLE `WECREATE_CLI_LOG` MODIFY COLUMN `TRACE_ID` VARCHAR(255) NOT NULL COMMENT '追踪标识';
ALTER TABLE `WECREATE_CLI_LOG` MODIFY COLUMN `TYPE` VARCHAR(255) NOT NULL COMMENT '日志类型';
ALTER TABLE `WECREATE_CLI_LOG` MODIFY COLUMN `CONTENT` LONGTEXT COMMENT '日志内容';
ALTER TABLE `WECREATE_CLI_LOG` MODIFY COLUMN `LM_USER` VARCHAR(50) COMMENT '最后更新人';
ALTER TABLE `WECREATE_CLI_LOG` MODIFY COLUMN `LM_TIME` TIMESTAMP NULL COMMENT '最后更新时间';


-- =====================================================
-- 表：WECREATE_CLI_PROMPT
-- 描述：CLI 提示词执行记录表
-- =====================================================
DROP TABLE IF EXISTS `WECREATE_CLI_PROMPT`;

CREATE TABLE `WECREATE_CLI_PROMPT` (
  `ID` BIGINT NOT NULL AUTO_INCREMENT,
  `CREATE_USER` VARCHAR(50) NOT NULL,
  `CREATE_TIME` TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
  `PROMPT_CONTENT` LONGTEXT,
  `EXECUTION_TIME` INT,
  `WORK_DIRECTORY` VARCHAR(500),
  `TRACE_ID` VARCHAR(255) NOT NULL,
  `REQUEST_IP` VARCHAR(50),
  `LM_USER` VARCHAR(50),
  `LM_TIME` TIMESTAMP NULL,
  `PROJECT_ID` VARCHAR(255),
  `SCORE` DECIMAL(3,1),
  `SCORE_USER` VARCHAR(50),
  `GIT_CHANGED_FILES` INT,
  `GIT_INSERTIONS` INT,
  `GIT_DELETIONS` INT,
  `GIT_TOTAL_CHANGES` INT,
  `DESCRIPTION` LONGTEXT,
  `INPUT_TOKENS` INT,
  `OUTPUT_TOKENS` INT,
  `TOTAL_TOKENS` INT,
  `CACHED_TOKENS` INT,
  `GIT_COMMIT_HASH` VARCHAR(100),
  PRIMARY KEY (`ID`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='CLI 提示词执行记录表';

