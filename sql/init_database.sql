ALTER TABLE `WECREATE_CLI_PROMPT` MODIFY COLUMN `DESCRIPTION` LONGTEXT COMMENT '描述信息';
ALTER TABLE `WECREATE_CLI_PROMPT` MODIFY COLUMN `INPUT_TOKENS` INT COMMENT '输入 Token 数量';
ALTER TABLE `WECREATE_CLI_PROMPT` MODIFY COLUMN `OUTPUT_TOKENS` INT COMMENT '输出 Token 数量';
ALTER TABLE `WECREATE_CLI_PROMPT` MODIFY COLUMN `TOTAL_TOKENS` INT COMMENT '总 Token 数量';
ALTER TABLE `WECREATE_CLI_PROMPT` MODIFY COLUMN `CACHED_TOKENS` INT COMMENT '命中缓存 Token 数量';
ALTER TABLE `WECREATE_CLI_PROMPT` MODIFY COLUMN `GIT_COMMIT_HASH` VARCHAR(100) COMMENT 'Git 提交哈希值，用于避免重复记录同一提交';


-- =====================================================
-- 表：WECREATE_PROJECT
-- 描述：项目管理表
-- =====================================================
DROP TABLE IF EXISTS `WECREATE_PROJECT`;

CREATE TABLE `WECREATE_PROJECT` (
  `ID` BIGINT NOT NULL AUTO_INCREMENT,
  `PROJECT_NAME` VARCHAR(100) NOT NULL,
  `GIT_URL` VARCHAR(500) NOT NULL,
  `LOCAL_PATH` VARCHAR(500) NOT NULL,
  `DESCRIPTION` VARCHAR(500),
  `CREATE_USER` VARCHAR(50) NOT NULL,
  `CREATE_TIME` TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
  `LM_USER` VARCHAR(50),
  `LM_TIME` TIMESTAMP NULL,
  `STATUS` INT DEFAULT 0,
  `REMARK` VARCHAR(500),
  PRIMARY KEY (`ID`),
  UNIQUE KEY `uk_project_name` (`PROJECT_NAME`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='项目管理表';

-- 添加列注释
ALTER TABLE `WECREATE_PROJECT` MODIFY COLUMN `ID` BIGINT NOT NULL AUTO_INCREMENT COMMENT '主键';
ALTER TABLE `WECREATE_PROJECT` MODIFY COLUMN `PROJECT_NAME` VARCHAR(100) NOT NULL COMMENT '项目名称';
ALTER TABLE `WECREATE_PROJECT` MODIFY COLUMN `GIT_URL` VARCHAR(500) NOT NULL COMMENT 'Git 仓库地址';
ALTER TABLE `WECREATE_PROJECT` MODIFY COLUMN `LOCAL_PATH` VARCHAR(500) NOT NULL COMMENT '本地绝对路径';
ALTER TABLE `WECREATE_PROJECT` MODIFY COLUMN `DESCRIPTION` VARCHAR(500) COMMENT '项目描述';
ALTER TABLE `WECREATE_PROJECT` MODIFY COLUMN `CREATE_USER` VARCHAR(50) NOT NULL COMMENT '创建人';
ALTER TABLE `WECREATE_PROJECT` MODIFY COLUMN `CREATE_TIME` TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL COMMENT '创建时间';
ALTER TABLE `WECREATE_PROJECT` MODIFY COLUMN `LM_USER` VARCHAR(50) COMMENT '最后更新人';
ALTER TABLE `WECREATE_PROJECT` MODIFY COLUMN `LM_TIME` TIMESTAMP NULL COMMENT '最后更新时间';
ALTER TABLE `WECREATE_PROJECT` MODIFY COLUMN `STATUS` INT DEFAULT 0 COMMENT '项目状态：0-未克隆，1-已克隆，2-克隆失败';
ALTER TABLE `WECREATE_PROJECT` MODIFY COLUMN `REMARK` VARCHAR(500) COMMENT '备注信息';
-- 系统配置表
CREATE TABLE IF NOT EXISTS SYSTEM_CONFIG (
    ID DECIMAL(20,0) PRIMARY KEY,
    CONFIG_KEY VARCHAR(100) NOT NULL COMMENT '配置键',
    CONFIG_VALUE VARCHAR(500) COMMENT '配置值',
    DESCRIPTION VARCHAR(200) COMMENT '配置描述',
    CREATE_USER VARCHAR(50) COMMENT '创建人',
    CREATE_TIME DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    LM_USER VARCHAR(50) COMMENT '最后更新人',
    LM_TIME DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '最后更新时间',
    UNIQUE KEY UK_CONFIG_KEY (CONFIG_KEY)
) COMMENT='系统配置表';
