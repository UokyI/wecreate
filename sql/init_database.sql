ALTER TABLE `WECREATE_CLI_PROMPT` MODIFY COLUMN `PROMPT_CONTENT` LONGTEXT COMMENT '提示词内容';
ALTER TABLE `WECREATE_CLI_PROMPT` MODIFY COLUMN `EXECUTION_TIME` INT COMMENT '执行耗时 (毫秒)';
ALTER TABLE `WECREATE_CLI_PROMPT` MODIFY COLUMN `WORK_DIRECTORY` VARCHAR(500) COMMENT '工作目录';
ALTER TABLE `WECREATE_CLI_PROMPT` MODIFY COLUMN `TRACE_ID` VARCHAR(255) NOT NULL COMMENT '追踪标识';
ALTER TABLE `WECREATE_CLI_PROMPT` MODIFY COLUMN `REQUEST_IP` VARCHAR(50) COMMENT '请求 IP';
ALTER TABLE `WECREATE_CLI_PROMPT` MODIFY COLUMN `LM_USER` VARCHAR(50) COMMENT '最后更新人';
ALTER TABLE `WECREATE_CLI_PROMPT` MODIFY COLUMN `LM_TIME` TIMESTAMP NULL COMMENT '最后更新时间';
ALTER TABLE `WECREATE_CLI_PROMPT` MODIFY COLUMN `PROJECT_ID` VARCHAR(255) COMMENT '项目 ID';
ALTER TABLE `WECREATE_CLI_PROMPT` MODIFY COLUMN `SCORE` DECIMAL(3,1) COMMENT '评分';
ALTER TABLE `WECREATE_CLI_PROMPT` MODIFY COLUMN `SCORE_USER` VARCHAR(50) COMMENT '评分人';
ALTER TABLE `WECREATE_CLI_PROMPT` MODIFY COLUMN `GIT_CHANGED_FILES` INT COMMENT 'Git 变更文件数';
ALTER TABLE `WECREATE_CLI_PROMPT` MODIFY COLUMN `GIT_INSERTIONS` INT COMMENT 'Git 新增行数';
